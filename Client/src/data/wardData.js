export const getWardColor = (score) => {
    if (score >= 75) return '#6F42C1';
    if (score >= 50) return '#A074F0';
    return '#D8B4FE';
};

// Function to get hypothetical ward performance (randomized for demo)
export const getWardPerformance = (wardId) => {
    const hash = wardId * 12345;
    return {
        score: 40 + (hash % 50),
        sanitation: 45 + (hash % 45),
        resolved: 60 + (hash % 35),
        pending: 10 + (hash % 90),
        trend: (hash % 3 === 0) ? 'up' : (hash % 3 === 1 ? 'down' : 'stable')
    };
};
